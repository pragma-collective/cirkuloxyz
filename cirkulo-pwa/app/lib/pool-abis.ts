/**
 * Get the correct pool ABI based on circle type
 */
export function getPoolAbi(circleType: "contribution" | "rotating" | "fundraising") {
  switch (circleType) {
    case "contribution":
      return savingsPoolAbi;
    case "rotating":
      return roscaPoolAbi;
    case "fundraising":
      return donationPoolAbi;
    default:
      throw new Error(`Unknown circle type: ${circleType}`);
  }
}

/**
 * Get the correct function name for contributions based on circle type
 */
export function getContributeFunctionName(circleType: "contribution" | "rotating" | "fundraising") {
  switch (circleType) {
    case "contribution":
      return "deposit";
    case "rotating":
      return "contribute";
    case "fundraising":
      return "donate";
    default:
      throw new Error(`Unknown circle type: ${circleType}`);
  }
}
